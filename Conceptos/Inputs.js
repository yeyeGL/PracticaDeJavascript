//input de usuario

let numero1, numero2, suma, resta, multiplicacion, division, modulo, potencia;

numero1 = parseInt(prompt('Ingrese el valor del numero 1'));
numero2 = parseInt(prompt('Ingrese el valor del numero 2'));

suma = numero1 + numero2;
resta = numero1 - numero2;
multiplicacion = numero1 * numero2;
division = numero1 / numero2;
modulo = numero1 % numero2;
potencia = numero1 ** numero2;

document.write(suma);
document.write('<br>')
document.write(resta);
document.write('<br>');
document.write(multiplicacion);
document.write('<br>');
document.write(division);
document.write('<br>');
document.write(modulo);
document.write('<br>');
document.write(potencia);
document.write('<br>');

